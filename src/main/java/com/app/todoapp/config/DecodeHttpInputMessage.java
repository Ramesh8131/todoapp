package com.app.todoapp.config;

import java.io.IOException;
import java.io.InputStream;

import org.springframework.core.MethodParameter;
import org.springframework.http.HttpHeaders;
import org.springframework.http.HttpInputMessage;

public class DecodeHttpInputMessage implements HttpInputRequesytMessage {

    private HttpHeaders headers;

    private InputStream body;
    
    private MethodParameter parameters;

    public DecodeHttpInputMessage(HttpHeaders headers, InputStream body,MethodParameter parameters) {
        super();
        this.headers = headers;
        this.body = body;
        this.parameters = parameters;
    }

    @Override
    public HttpHeaders getHeaders() {
        return this.headers;
    }

    @Override
    public InputStream getBody() throws IOException {
        return this.body;
    }

	@Override
	public MethodParameter getParameter() throws IOException {
		 return this.parameters;
	}
}
